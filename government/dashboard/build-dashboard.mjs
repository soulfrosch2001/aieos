#!/usr/bin/env node
// Government enterprise dashboard generator.
// Read-only: scans the registry + each mounted company and writes dashboard.md.
// It writes nothing inside any company (Directive #2 — the Government aggregates,
// it never computes a company's numbers for it). Run: npm run dashboard
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(HERE, '../../');
const NON_COMPANY = new Set([
  'kernel', 'government', 'templates', 'workflows', 'councils', 'memory', 'shared',
  'docs', 'examples', 'tests', 'scripts', 'companies', 'roadmap',
  '.git', '.github', '.claude', 'node_modules',
]);

const read = (p) => fs.readFileSync(p, 'utf8');
const exists = (p) => fs.existsSync(p);
const childDirs = (d) => fs.readdirSync(d, { withFileTypes: true }).filter((e) => e.isDirectory()).map((e) => e.name);
const childFiles = (d) => { try { return fs.readdirSync(d, { withFileTypes: true }).filter((e) => e.isFile()).map((e) => e.name); } catch { return []; } };

function walkDirs(dir) {
  const out = [];
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    if (!e.isDirectory()) continue;
    const p = path.join(dir, e.name);
    out.push(p, ...walkDirs(p));
  }
  return out;
}

const kernelVersion = read(path.join(ROOT, 'kernel', 'VERSION')).trim();

// Discover companies: a top-level dir holding an AIEOS.md adapter (root or 00-company).
const companies = [];
for (const name of childDirs(ROOT)) {
  if (NON_COMPANY.has(name)) continue;
  const nativeAdapter = path.join(ROOT, name, '00-company', 'AIEOS.md');
  const legacyAdapter = path.join(ROOT, name, 'AIEOS.md');
  let adapter = null, kind = null;
  if (exists(nativeAdapter)) { adapter = nativeAdapter; kind = 'kernel-native'; }
  else if (exists(legacyAdapter)) { adapter = legacyAdapter; kind = 'legacy (adapter)'; }
  else continue;

  const root = path.join(ROOT, name);
  const dirs = walkDirs(root);
  const agents = dirs.filter((d) => childFiles(d).includes('craft.md')).length;
  const councils = dirs.filter((d) => childFiles(d).includes('process.md')).length;
  const m = read(adapter).match(/Requires kernel:\s*([^\n]+)/i);
  const kernelReq = m ? m[1].trim() : '—';
  const health = exists(path.join(root, 'reports', 'health-report.md')) ? `../../${name}/reports/health-report.md` : null;

  companies.push({ name, kind, kernelReq, agents, councils, health });
}
companies.sort((a, b) => (a.kind === b.kind ? a.name.localeCompare(b.name) : a.kind.localeCompare(b.kind)));

const tA = companies.reduce((s, c) => s + c.agents, 0);
const tC = companies.reduce((s, c) => s + c.councils, 0);

const rows = companies.map((c) => {
  const link = `[${c.name}](../../${c.name}/)`;
  const health = c.health ? `[health](${c.health})` : '—';
  return `| ${link} | ${c.kind} | ${c.kernelReq} | ${c.agents} | ${c.councils} | ${health} |`;
});

const out = `# Enterprise Dashboard

> Generated by \`npm run dashboard\` — a read-only roll-up via the
> [reporting protocol](../../kernel/protocols/reporting.md). Do not edit by hand.

**Kernel:** ${kernelVersion}  ·  **Companies mounted:** ${companies.length}  ·  **Agents:** ${tA}  ·  **Councils:** ${tC}

| Company | Mount | Kernel req | Agents | Councils | Health |
|---------|-------|-----------|:------:|:--------:|:------:|
${rows.join('\n')}
| **Total** | — | — | **${tA}** | **${tC}** | — |

See [README.md](README.md) to regenerate, and
[../../kernel/registry/registry.md](../../kernel/registry/registry.md) for the canonical
registry.
`;

fs.writeFileSync(path.join(HERE, 'dashboard.md'), out);
console.log(`Dashboard written: ${companies.length} companies, ${tA} agents, ${tC} councils (kernel ${kernelVersion}).`);
